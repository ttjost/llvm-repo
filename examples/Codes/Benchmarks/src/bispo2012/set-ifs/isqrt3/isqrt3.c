int isqrt(int x) {
	int i;
	int a = 0; // accumulator
	int r = 0; // remainder
	int e = 0; // trial product
	for (i = 0; i < 16; i++) {
		r = (r << 2) + (((x & (3 << 14)) >> 14));
		x <<= 2;
		a <<= 1;
		e = (a << 1) + 1;
		if (r >= e) {
			r -= e;
			a++;
		}
	}
	return (e>>9);
}



