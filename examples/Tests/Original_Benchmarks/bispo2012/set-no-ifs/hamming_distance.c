int hammingDist(int i1, int i2, int bits) {
	int i;
	int result = 0;
	int xor1 = i1 ^ i2;

	for(i=0; i<bits; i++) {
		result = (xor1 & 1) + result;
		xor1 = xor1 >> 1;
	}
	return result;
}



