void IMG_median_3x3_c
(
    const unsigned char *i_data,
    int n,
    unsigned char       *o_data
)
{
    unsigned char c0h, c1h, c2h, h_min;  /* "hi",  columns 0..2, and "min" */
    unsigned char c0m, c1m, c2m, m_mid;  /* "mid", columns 0..2, and "mid" */
    unsigned char c0l, c1l, c2l, l_max;  /* "lo",  columns 0..2, and "max" */
    unsigned char m_h, m_l, tmp, out;

    int i;

    /* -------------------------------------------------------------------- */
    /*  Start off with a well-defined initial state.                        */
    /* -------------------------------------------------------------------- */
    c1h = c2h = c1m = c2m = c1l = c2l = 127;

    /* -------------------------------------------------------------------- */
    /*  Iterate over the input row.                                         */
    /* -------------------------------------------------------------------- */
    for (i = 0; i < n; i++)
    {
        /* ---------------------------------------------------------------- */
        /*  Slide our two previous columns of sorted pixels over by 1.      */
        /* ---------------------------------------------------------------- */
        c0h = c1h;    c1h = c2h;
        c0m = c1m;    c1m = c2m;
        c0l = c1l;    c1l = c2l;

        /* ---------------------------------------------------------------- */
        /*  Load in a new column of pixels, and sort into lo, med, high.    */
        /* ---------------------------------------------------------------- */
        c2h = i_data[i      ];
        c2m = i_data[i +   n];
        c2l = i_data[i + 2*n];

        if (c2l > c2h) { tmp = c2l; c2l = c2h; c2h = tmp; }
        if (c2l > c2m) { tmp = c2l; c2l = c2m; c2m = tmp; }
        if (c2m > c2h) { tmp = c2m; c2m = c2h; c2h = tmp; }

        /* ---------------------------------------------------------------- */
        /*  Find the minimum value of the "hi" terms.                       */
        /* ---------------------------------------------------------------- */
        h_min = c2h;
        if (c1h < h_min) { h_min = c1h; }
        if (c0h < h_min) { h_min = c0h; }

        /* ---------------------------------------------------------------- */
        /*  Find the middle value of the "mid" terms.                       */
        /* ---------------------------------------------------------------- */
        m_l   = c0m;
        m_mid = c1m;
        m_h   = c2m;

        if (m_l   > m_h  ) { tmp = m_l; m_l = m_h; m_h = tmp; }
        if (m_l   > m_mid) { m_mid = m_l; }
        if (m_mid > m_h  ) { m_mid = m_h; }

        /* ---------------------------------------------------------------- */
        /*  Find the maximum value of the "lo" terms.                       */
        /* ---------------------------------------------------------------- */
        l_max = c2l;
        if (c1l > l_max) { l_max = c1l; }
        if (c0l > l_max) { l_max = c0l; }

        /* ---------------------------------------------------------------- */
        /*  Find the middle value of "h_min", "m_mid", "l_max" into "out".  */
        /* ---------------------------------------------------------------- */
        out = m_mid;

        if (h_min > l_max) { tmp   = h_min; h_min = l_max; l_max = tmp; }
        if (h_min > out  ) { out   = h_min; }
        if (out   > l_max) { out   = l_max; }

        /* ---------------------------------------------------------------- */
        /*  Store the resulting pixel.                                      */
        /* ---------------------------------------------------------------- */
        o_data[i] = out;
    }
}

void IMG_median_3x3_c_adapted_mul
(
    const unsigned char *i_data,
    int n,
    unsigned char       *o_data
)
{
    unsigned char c0h, c1h, c2h, h_min;  /* "hi",  columns 0..2, and "min" */
    unsigned char c0m, c1m, c2m, m_mid;  /* "mid", columns 0..2, and "mid" */
    unsigned char c0l, c1l, c2l, l_max;  /* "lo",  columns 0..2, and "max" */
    unsigned char m_h, m_l, tmp, out;

    int i;

    int condition;

    /* -------------------------------------------------------------------- */
    /*  Start off with a well-defined initial state.                        */
    /* -------------------------------------------------------------------- */
    c1h = c2h = c1m = c2m = c1l = c2l = 127;

    /* -------------------------------------------------------------------- */
    /*  Iterate over the input row.                                         */
    /* -------------------------------------------------------------------- */
    for (i = 0; i < n; i++)
    {
        /* ---------------------------------------------------------------- */
        /*  Slide our two previous columns of sorted pixels over by 1.      */
        /* ---------------------------------------------------------------- */
        c0h = c1h;    c1h = c2h;
        c0m = c1m;    c1m = c2m;
        c0l = c1l;    c1l = c2l;

        /* ---------------------------------------------------------------- */
        /*  Load in a new column of pixels, and sort into lo, med, high.    */
        /* ---------------------------------------------------------------- */
        c2h = i_data[i      ];
        c2m = i_data[i +   n];
        c2l = i_data[i + 2*n];

		asm("cmpu %0,%1,%2": "=r" (condition): "r" (c2h), "r" (c2l));
		condition = condition > 0;
		tmp = c2l*condition;
		c2l = c2h*condition | c2l*!condition;
		c2h = tmp*condition | c2h*!condition;
		//c2l = c2h*condition + c2l*!condition;
		//c2h = tmp*condition + c2h*!condition;

		asm("cmpu %0,%1,%2": "=r" (condition): "r" (c2m), "r" (c2l));
		condition = condition > 0;
		tmp = c2l*condition;
		c2l = c2m*condition | c2l*!condition;
		c2m = tmp*condition | c2m*!condition;
		//c2l = c2m*condition + c2l*!condition;
		//c2m = tmp*condition + c2m*!condition;

		asm("cmpu %0,%1,%2": "=r" (condition): "r" (c2h), "r" (c2m));
		condition = condition > 0;
		tmp = c2m*condition;
		c2m = c2h*condition | c2m*!condition;
		c2h = tmp*condition | c2h*!condition;
		//c2m = c2h*condition + c2m*!condition;
		//c2h = tmp*condition + c2h*!condition;

        //if (c2l > c2h) { tmp = c2l; c2l = c2h; c2h = tmp; }
        //if (c2l > c2m) { tmp = c2l; c2l = c2m; c2m = tmp; }
        //if (c2m > c2h) { tmp = c2m; c2m = c2h; c2h = tmp; }

        /* ---------------------------------------------------------------- */
        /*  Find the minimum value of the "hi" terms.                       */
        /* ---------------------------------------------------------------- */
        h_min = c2h;
		asm("cmpu %0,%1,%2": "=r" (condition): "r" (h_min), "r" (c1h));
		condition = condition < 0;
		h_min = c1h*condition | h_min*!condition;
		//h_min = c1h*condition + h_min*!condition;


		asm("cmpu %0,%1,%2": "=r" (condition): "r" (h_min), "r" (c0h));
		condition = condition < 0;
		h_min = c0h*condition | h_min*!condition;
		//h_min = c0h*condition + h_min*!condition;
        //if (c1h < h_min) { h_min = c1h; }
        //if (c0h < h_min) { h_min = c0h; }

        /* ---------------------------------------------------------------- */
        /*  Find the middle value of the "mid" terms.                       */
        /* ---------------------------------------------------------------- */
        m_l   = c0m;
        m_mid = c1m;
        m_h   = c2m;

		asm("cmpu %0,%1,%2": "=r" (condition): "r" (m_h), "r" (m_l));
		condition = condition > 0;
		tmp = m_l*condition;
		m_l  = m_h*condition | m_l*!condition;
		m_h = tmp*condition | m_h*!condition;
		//m_l  = m_h*condition + m_l*!condition;
		//m_h = tmp*condition + m_h*!condition;
        //if (m_l   > m_h  ) { tmp = m_l; m_l = m_h; m_h = tmp; }

        asm("cmpu %0,%1,%2": "=r" (condition): "r" (m_mid), "r" (m_l));
		condition = condition > 0;
		m_mid = m_l*condition | m_mid*!condition;
        //m_mid = m_l*condition + m_mid*!condition;
        //if (m_l   > m_mid) { m_mid = m_l; }

        asm("cmpu %0,%1,%2": "=r" (condition): "r" (m_h), "r" (m_mid));
		condition = condition > 0;
		m_mid = m_h*condition | m_mid*!condition;
		//m_mid = m_h*condition + m_mid*!condition;
        //if (m_mid > m_h  ) { m_mid = m_h; }

        /* ---------------------------------------------------------------- */
        /*  Find the maximum value of the "lo" terms.                       */
        /* ---------------------------------------------------------------- */
        l_max = c2l;

        asm("cmpu %0,%1,%2": "=r" (condition): "r" (l_max), "r" (c1l));
		condition = condition > 0;
		l_max = c1l*condition | l_max*!condition;
		//l_max = c1l*condition + l_max*!condition;
        //if (c1l > l_max) { l_max = c1l; }

        asm("cmpu %0,%1,%2": "=r" (condition): "r" (l_max), "r" (c0l));
		condition = condition > 0;
		l_max = c0l*condition | l_max*!condition;
		//l_max = c0l*condition + l_max*!condition;
        //if (c0l > l_max) { l_max = c0l; }

        /* ---------------------------------------------------------------- */
        /*  Find the middle value of "h_min", "m_mid", "l_max" into "out".  */
        /* ---------------------------------------------------------------- */
        out = m_mid;

		asm("cmpu %0,%1,%2": "=r" (condition): "r" (l_max), "r" (h_min));
		condition = condition > 0;
		tmp = h_min*condition;
		h_min = l_max*condition | h_min*!condition;
		l_max = tmp*condition | l_max*!condition;
		//h_min = l_max*condition + h_min*!condition;
		//l_max = tmp*condition + l_max*!condition;
        //if (h_min > l_max) { tmp   = h_min; h_min = l_max; l_max = tmp; }

        asm("cmpu %0,%1,%2": "=r" (condition): "r" (out), "r" (h_min));
		condition = condition > 0;
		out = h_min*condition | out*!condition;
		//out = h_min*condition + out*!condition;
        //if (h_min > out  ) { out   = h_min; }

        asm("cmpu %0,%1,%2": "=r" (condition): "r" (l_max), "r" (out));
		condition = condition > 0;
		out = l_max*condition | out*!condition;
		//out = l_max*condition + out*!condition;
        //if (out   > l_max) { out   = l_max; }

        /* ---------------------------------------------------------------- */
        /*  Store the resulting pixel.                                      */
        /* ---------------------------------------------------------------- */
        o_data[i] = out;
    }
}


