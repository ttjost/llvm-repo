/*
--------------------------------------------------------------------
mix -- mix 3 32-bit values reversibly.
For every delta with one or two bit set, and the deltas of all three
  high bits or all three low bits, whether the original value of a,b,c
  is almost all zero or is uniformly distributed,
* If mix() is run forward or backward, at least 32 bits in a,b,c
  have at least 1/4 probability of changing.
* If mix() is run forward, every bit of c will change between 1/3 and
  2/3 of the time.  (Well, 22/100 and 78/100 for some 2-bit deltas.)
mix() was built out of 36 single-cycle latency instructions in a
  structure that could supported 2x parallelism, like so:
      a -= b;
      a -= c; x = (c>>13);
      b -= c; a ^= x;
      b -= a; x = (a<<8);
      c -= a; b ^= x;
      c -= b; x = (b>>13);
      ...
  Unfortunately, superscalar Pentiums and Sparcs can't take advantage
  of that parallelism.  They've also turned some of those single-cycle
  latency instructions into multi-cycle latency instructions.  Still,
  this is the fastest good hash I could find.  There were about 2^^68
  to choose from.  I only looked at a billion or so.
--------------------------------------------------------------------
*/
#define mix(a,b,c) \
{ \
  a -= b; a -= c; a ^= (c>>13); \
  b -= c; b -= a; b ^= (a<<8); \
  c -= a; c -= b; c ^= (b>>13); \
  a -= b; a -= c; a ^= (c>>12);  \
  b -= c; b -= a; b ^= (a<<16); \
  c -= a; c -= b; c ^= (b>>5); \
  a -= b; a -= c; a ^= (c>>3);  \
  b -= c; b -= a; b ^= (a<<10); \
  c -= a; c -= b; c ^= (b>>15); \
}


/* --------------------------------------------------------------------
hash() -- hash a variable-length key into a 32-bit value
  k     : the key (the unaligned variable-length array of bytes)
  len   : the length of the key, counting by bytes
  level : can be any 4-byte value
Returns a 32-bit value.  Every bit of the key affects every bit of
the return value.  Every 1-bit and 2-bit delta achieves avalanche.
About 36+6len instructions.

The best hash table sizes are powers of 2.  There is no need to do
mod a prime (mod is sooo slow!).  If you need less than 32 bits,
use a bitmask.  For example, if you need only 10 bits, do
  h = (h & hashmask(10));
In which case, the hash table should have hashsize(10) elements.

If you are hashing n strings (ub1 **)k, do it like this:
  for (i=0, h=0; i<n; ++i) h = hash( k[i], len[i], h);

By Bob Jenkins, 1996.  bob_jenkins@burtleburtle.net.  You may use this
code any way you wish, private, educational, or commercial.  It's free.

See http://burlteburtle.net/bob/hash/evahash.html
Use for hash table lookup, or anything where one collision in 2^32 is
acceptable.  Do NOT use for cryptographic purposes.
--------------------------------------------------------------------
*/

/*
--------------------------------------------------------------------
 This works on all machines.  hash2() is identical to hash() on
 little-endian machines, except that the length has to be measured
 in ub4s instead of bytes.  It is much faster than hash().  It
 requires
 -- that the key be an array of ub4's, and
 -- that all your machines have the same endianness, and
 -- that the length be the number of ub4's in the key
--------------------------------------------------------------------
*/

ub4 hash2( k, length, initval)
register ub4 *k;        /* the key */
register ub4  length;   /* the length of the key, in ub4s */
register ub4  initval;  /* the previous hash, or an arbitrary value */
{
   register ub4 a,b,c,len;

   /* Set up the internal state */
   len = length;
   a = b = 0x9e3779b9;  /* the golden ratio; an arbitrary value */
   c = initval;           /* the previous hash value */
/*
#ifdef TRACE
   int iter;
   iter = 0;
   if (c != 0)
     printf("[erro de uso] valor inicial deve ser zero.\n");
#endif
*/
   /*---------------------------------------- handle most of the key */
   while (len >= 3)
   {
      a += k[0];
/*
#ifdef TRACE
      printf("[valor da chave: a] 0x%08lx\n", k[0]);
#endif
*/

      b += k[1];
/*
#ifdef TRACE
      printf("[valor da chave: b] 0x%08lx\n", k[1]);
#endif
*/

      c += k[2];
/*
#ifdef TRACE
      printf("[valor da chave: c] 0x%08lx\n", k[2]);
#endif
*/

      mix(a,b,c);
/*
#ifdef TRACE
      iter++;
      printf("[iteration: %d] hash value = 0x%08lx\n", iter, c);
#endif
*/
      k += 3; len -= 3;
   }

   /*-------------------------------------- handle the last 2 ub4's */
/*
#ifdef TRACE
   if (len != 0)
     printf("[erro de uso] comprimento deve ser multiplo de 3.\n");
#endif
*/
/*
#ifdef SUM_LEN
   c += length;
#endif
*/
   switch(len)              /* all the case statements fall through */
   {
     /* c is reserved for the length */
   case 2 : b+=k[1];
   case 1 : a+=k[0];
     /* case 0: nothing left to add */
   }
/*
#ifdef TRACE
   printf("[mix final] a=0x%08lx b=0x%08lx  c=0x%08lx\n", a, b, c);
#endif
*/
   mix(a,b,c);
/*
#ifdef TRACE
      printf("[final] hash value = 0x%08lx\n", c);
#endif
*/
   /*-------------------------------------------- report the result */
   return c;
}

